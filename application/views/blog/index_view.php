            <h2 class="blog-post-title">LÀM THẾ NÀO ĐỂ MUA ĐƯỢC HÀNG GIẢM GIÁ TẠI NHẬT BẰNG PIKASHIPPER?</h2>
            <p class="blog-post-meta">October 2, 2016 by <a href="http://getbootstrap.com/examples/blog/#">Mai</a></p>

            <p>Hàng giảm giá ở Nhật chỉ giảm giá trong thời gian ngắn, ví dụ 24h hay 72 giờ và số lượng thì có hạn.

               Để có thể ngồi ở Việt Nam và mua được hàng giảm giá, bạn cần phải thao tác nhanh nhẹn, chính xác.</p>
            <hr>
            <p>Bước 1: Tìm được món đồ mình thích, ngay lập tức đăng Yêu Cầu Mua Hộ lên PikaShipper.</p>
            <blockquote>
              <p>PikaShipper sẽ tự động gửi thông báo đến tất cả Người Mua Hộ, bảo họ giúp bạn. Tuy nhiên, họ sẽ chỉ giúp bạn nếu Tiền Công bạn đưa ra tốt, ngày giao đồ không quá gấp rút. PikaShipper thì nghĩ rằng, vì bạn mua được đồ giảm giá thì cũng nên đưa ra Tiền Công hậu hĩnh chút cho Người Mua Hộ. Đôi bên cũng có lợi.</p>
            </blockquote>
            <p>Bước 2: Ngay sau khi có Người Mua Hộ nhận mua giúp bạn, bạn hãy nhanh chóng thanh toán tiền đến PikaShipper. Lý do là, chỉ sau khi bạn thanh toán tiền thì Người Mua Hộ mới mua đồ cho bạn. Mà thời gian để mua hàng giảm giá rất gấp rút, nên bạn cần phải nhanh.</p>
  
            <p>Bước 3: Khi nhận đồ từ Người Mua Hộ, hãy kiểm tra hàng hóa cẩn thận, bấm nút “Tôi đã nhận được đồ. Tôi đồng ý trả tiền cho Người Mua Hộ”, và không quên đánh giá/bình luận dịch vụ của Người Mua Hộ nhé.</p>
            <blockquote>
				<p>Nếu Người Mua Hộ không mua kịp thì sao? Nếu không mua kịp hàng sale off, nghĩa là giá sản phẩm tăng lên, Người Mua Hộ sẽ chủ động hủy Đề Nghị Nhận Mua Hộ và bạn nhận lại tiền. Chú ý: Khi PikaShipper chuyển tiền lại cho bạn, phí chuyển tiền là do Người Yêu Cầu trả ( Nên bạn sẽ mất từ 3000 VND ~ 11000 VND do chi phí chuyển khoản này)</p>
            </blockquote>
			
